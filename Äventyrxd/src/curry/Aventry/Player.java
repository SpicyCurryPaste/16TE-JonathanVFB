package curry.Aventry;

import java.util.Scanner;

public class Player {
	String name;
	int age, present, birth;

	public Player(int age, String name, int present, int birth) {
		this.age = age;
		this.name = name;
		this.present = present;
		this.birth = birth;
	
		
	}
	
	public int getAge(){
		return this.age;
		
	}
	public String getName(){
		return this.name;
		
	}
	

}
